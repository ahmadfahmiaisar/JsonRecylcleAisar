package com.bismillah.jsonrecylcleaisar;

public class MixData {
private String valuesatu, valuedua, valuetiga, valueempat;

    public MixData(String valuesatu, String valuedua, String valuetiga, String valueempat) {
        this.valuesatu = valuesatu;
        this.valuedua = valuedua;
        this.valuetiga = valuetiga;
        this.valueempat = valueempat;
    }

    public String getValuesatu() {
        return valuesatu;
    }

    public String getValuedua() {
        return valuedua;
    }

    public String getValuetiga() {
        return valuetiga;
    }

    public String getValueempat() {
        return valueempat;
    }
}
